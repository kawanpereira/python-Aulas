"""
Operadores aritméticos
+  - Adição
_  - Subtração
*  - Multiplicação
/  - Divisão
// - Divisão inteira
** - Exponenciação
%  - Resto da divisão
() -
"""

print(10 + 2)
print(10 - 2)
print(10 * 2)
print(10 / 2)
print(10 // 2)
print(10 ** 2)
print(10 % 3)
print(10 + (2 - 1))

